    public NauczycielZakForm(String login, String NazwaKlasy) throws SQLException, ClassNotFoundException {
        ArrayList<String> listaPrzedmiotow = new ArrayList<String>();
        this.user=login;
        tm = new tModelNauZak();
        tm.readTables(login, NazwaKlasy, "","");
        this.nazwaKlasy = NazwaKlasy;
        initComponents();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");

             //conn = null;
            Connection conn = DriverManager.getConnection(DBConnector.url,DBConnector.username, DBConnector.password);
            String query3 = "SELECT p.nazwaPrz FROM przypisnau pn "
                    + "JOIN nauczyciel n ON pn.idNau=n.idNau "
                    + "JOIN przedmiot p ON pn.idPrzed=p.idPrzed "
                    + "JOIN klasa k ON pn.idklasa=k.idklasa "
                    + "WHERE n.nazwaUz=? AND nazwaklasa=?";
            
            PreparedStatement preparedStmt3 = conn.prepareStatement(query3);
            preparedStmt3.setString(1, login);
            preparedStmt3.setString(2, NazwaKlasy);
            ResultSet rs3 = preparedStmt3.executeQuery();
            
            while (rs3.next())
                listaPrzedmiotow.add(rs3.getString("p.nazwaPrz"));
            conn.close();
            rs3.close();
            for (int i = 0; i < listaPrzedmiotow.size(); i++)
                jWybierzPrzemdiotComboBox.addItem(listaPrzedmiotow.get(i));
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(400);
        
        
        
    }

