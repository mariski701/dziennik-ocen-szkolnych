    public boolean isAlpha(String name) {
        char[] chars = name.toCharArray();

        for (char c : chars) {
            if(!Character.isLetter(c)) {
                return false;
            }
        }

        return true;
    }
    
    public boolean isMailValid(String text)
    {
        char[] chars = text.toCharArray();
        for (int i = 0; i < text.length(); i++)
        {
            if (chars[i] == '.')
                return true;
        }
        return false;
        
    }
    
    
