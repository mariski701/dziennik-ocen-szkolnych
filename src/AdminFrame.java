    private void jUsunNauczycielaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jUsunNauczycielaButtonActionPerformed
        ArrayList<String> listaLoginowNauczycieli = new ArrayList<String>();
        try
        {
            String login;
            Class.forName("com.mysql.jdbc.Driver");

             //conn = null;
            Connection conn = DriverManager.getConnection(DBConnector.url,DBConnector.username, DBConnector.password);
            
            String query = "SELECT nazwaUz FROM nauczyciel group by idNau";
            
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next())
            {
                login = rs.getString("nazwaUz");
                listaLoginowNauczycieli.add(login);
            }
            listaLoginowNauczycieli.remove("nauzapas");
            st.close();
            conn.close();
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
        for (int i = 0; i < listaLoginowNauczycieli.size(); i++)
        {
            jUsunNauczycielaComboBox.addItem(listaLoginowNauczycieli.get(i));
        }
        
        jUsunNauczycielaDialog.pack();
        jUsunNauczycielaDialog.setLocationRelativeTo(null);
        jUsunNauczycielaDialog.setVisible(true);
    }//GEN-LAST:event_jUsunNauczycielaButtonActionPerformed

